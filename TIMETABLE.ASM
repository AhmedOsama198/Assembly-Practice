READSTR MACRO  INPUT
mov ah,07
int 21h
LEA DI,INPUT  
MOV [DI],AL
INC DI
INT 21H            
MOV [DI],AL            
ENDM READSTR

.CODE 


MAIN PROC FAR 
MOV AX,@DATA
MOV DS,AX 
    

; PRINTING DAYS 
MOV AX,DAYSCOUNT 
MOV CL,STRLENGTH
DIV CL ; DIVIDING THE LENGTH OF THE ARRAY BY 3 TO GET THE COUNT OF THE STRINGS
MOV CX , AX  
LEA SI,DAYS 
MOV DX, 0 
DLOOP:
    MOV BL,0CH ; RED COLOR
    MOVECURSORRIGHT 8  
    DISPLAYSTR  
    INC SI 
    LOOP DLOOP 




; PRINTING SESSIONS VERTICAL COL 
LEA SI, SESSIONS
MOV AX,SESSIONSCOUNT 
MOV CL,STRLENGTH
DIV CL ; DIVIDING THE LENGTH OF THE ARRAY BY 3 TO GET THE COUNT OF THE STRINGS
MOV CX , AX 
MOV DX,0 
SLOOP:
    MOV BL,0AH ; GREEN COLOR
    MOVECURSORDOWNSTARTLINE 3  
    DISPLAYSTR  
    INC SI 
    LOOP SLOOP 

;PRINTING THE LECTURES 


LEA SI, LECTURES
MOV CX , LECTURESCOUNT 
MOV DX,0 
MOVECURSORDOWN 3 
MOVECURSORRIGHT 6
L1LOOP:
    MOV BL,0FH ; WHITE COLOR
    CMP [SI], 0 
    JE NOLECTURE
    DISPLAYSTR2
    MOVECURSORLEFT 6 
    NOLECTURE:
    MOVECURSORDOWN 3
    INC SI
    CMP DH,18H
    JGE NEXTDAY      
    LOOP L1LOOP 


NEXTDAY: 
MOVECURSORRIGHT 10 
MOV DH,3
MOVECURSOR
LOOP L1LOOP
 










                         
  
BYEBYE :
HLT
RET
MAIN ENDP
END MAIN
