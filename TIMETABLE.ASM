.MODEL small
.STACK 64

.DATA 
 
 
STRLENGTH EQU 3
Days db 'MON','TUS','WED','THU','SAT'
DAYSCOUNT EQU $-DAYS
Sessions db '1ST','2ND','3RD' ,'4TH' ,'5TH' ,'6TH' ,'7TH' 
SESSIONSCOUNT EQU $-SESSIONS
lectures db 'GENN001',0,0,'GENN001','CMPN201','GENN001','GENN001','GENN001','GENN001','GENN001',0,'CMPN201','GENN001','GENN001','GENN001','GENN001','GENN001','GENN001','CMPN201','GENN001','GENN001','GENN001',0,'GENN001','GENN001','CMPN201','GENN001','GENN001','GENN001','GENN001','GENN001',0,'CMPN201','GENN001','GENN001'
LECTURESCOUNT EQU $-LECTURES



MOVECURSOR MACRO  
MOV AH,2  
INT 10H 

ENDM MOVECURSOR


MOVECURSORRIGHT MACRO VAL 
mov ah,2
ADD DL,VAL
int 10h
ENDM MOVECURSORRIGHT

MOVECURSORLEFT MACRO VAL 
mov ah,2
SUB DL,VAL
int 10h
ENDM MOVECURSORLEFT
  

MOVECURSORDOWN MACRO VAL
mov ah,2
ADD DH,VAL
int 10h
ENDM MOVECURSORDOWN   

MOVECURSORDOWNSTARTLINE MACRO VAL
mov ah,2
ADD DH,VAL
MOV DL,0
int 10h
ENDM MOVECURSORDOWNSTARTLINE


DISPLAYSTR MACRO 
PUSH CX
mov ah,9 ;Display
mov bh,0 ;Page 0
mov al,[SI] 
mov cx,1h 
int 10h 
MOVECURSORRIGHT 1 
INC SI 
mov ah,9 ;Display
mov bh,0 ;Page 0
mov al,[SI] 
mov cx,1h ;1 times
int 10h
MOVECURSORRIGHT  1
INC SI 
mov ah,9 ;Display
mov bh,0 ;Page 0
mov al,[SI] 
mov cx,1h ;1 times
int 10h  
POP CX
ENDM DISPLAYSTR


